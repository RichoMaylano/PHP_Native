<?php 

function penjumlahan($a, $b){
    return $a+$b;
}

function pengurangan($a, $b){
    return $a-$b;
}

function perkalian($a, $b){
    return $a*$b;
}

function pembagian($a, $b){
    return $a/$b;
}

function modulus($a, $b){
    return $a%$b;
}

function pangkat($a, $b){
    return $a**$b;
}

?>